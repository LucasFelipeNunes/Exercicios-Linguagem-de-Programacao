public class TestaTiposPrimitivos
{
	public static void main (String[] args)
	{
		byte inteiroDeUmByte = 13;
		short inteiroDeDoisBytes = 132;
		int inteiroDeQuatroBytes = 132322;
		long inteiroDeOitoBytes = 10123456789;
		float numeroComVirgulaDeQuatroBytes = 3.01f;
		double numeroComVirgulaDeOitoBytes = 3.0000000001;
		char caractere = 'a';
		String texto = "aaaaaaaaaaaaaaa";
		System.out.println(inteiroDeUmByte);
		System.out.println(inteiroDeDoisBytes);
		System.out.println(inteiroDeQuatroBytes);
		System.out.println(inteiroDeOitoBytes);
		System.out.println(numeroComVirgulaDeQuatroBytes); 
		System.out.println(numeroComVirgulaDeOitoBytes);
		System.out.println(caractere);
		System.out.println(texto);
	}
}
